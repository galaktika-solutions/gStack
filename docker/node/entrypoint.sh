#!/bin/bash
set -e

################################################################################
if [ "$1" = 'npm' ]; then
  exec gprun -u "$(stat -c %u:%g .)" -s SIGINT "$@"
fi
################################################################################
if [ "$1" = 'watch' ]; then
  exec gprun -u "$(stat -c %u:%g .)" -s SIGINT webpack --mode development --watch
fi
################################################################################

exec "$@"
